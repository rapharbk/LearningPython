#While Loop

i = 1

# while condition 
while i <= 10:
    print(i)
    i += 1

print("Done with loop")
print("")



#For loop

for letter in "Giraffe Academy":
    print(letter)

print("")


friends = ["Jim", "Karen", "Kevin"]
for names in friends:
    print(names)

print("")


for index in range(10):                                      # 0 ~~ 9        its start in 0
    print(index)

print("")


for index in range(3,10):                                   # 3 ~~ 9
    print(index)

print("")


for index in range(len(friends)):                           #len()  imprime o tamanho da lista de amigos
    print(index)

print("")